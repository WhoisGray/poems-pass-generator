// آوانگاری فارسی → فینگلیش (برای ورودی‌های فارسی خام که آوانگاری ندارند)
// نکته: آوانگاری خودکار حدسی است؛ هر جا ممکن است از فیلد fg دیتابیس استفاده کنید.

const MAP = {
  'ا': 'a', 'آ': 'a', 'أ': 'a', 'إ': 'e', 'ء': '',
  'ب': 'b', 'پ': 'p', 'ت': 't', 'ث': 's', 'ج': 'j', 'چ': 'ch',
  'ح': 'h', 'خ': 'kh', 'د': 'd', 'ذ': 'z', 'ر': 'r', 'ز': 'z',
  'ژ': 'zh', 'س': 's', 'ش': 'sh', 'ص': 's', 'ض': 'z', 'ط': 't',
  'ظ': 'z', 'ع': 'a', 'غ': 'gh', 'ف': 'f', 'ق': 'gh', 'ک': 'k',
  'گ': 'g', 'ل': 'l', 'م': 'm', 'ن': 'n', 'و': 'v', 'ه': 'h',
  'ی': 'y', 'ي': 'y', 'ئ': 'y',
  'َ': 'a', 'ِ': 'e', 'ُ': 'o', 'ّ': '', 'ْ': '', 'ً': 'an',
  '‌': ' '
};

/** متن فارسی را به فینگلیش ساده تبدیل می‌کند */
export function transliterate(text) {
  let out = '';
  for (const ch of text) {
    if (MAP[ch] !== undefined) out += MAP[ch];
    else if (ch === ' ') out += ' ';
    else if (/[a-z0-9]/i.test(ch)) out += ch;
    // بقیه نویسه‌ها نادیده گرفته می‌شوند
  }
  // تمیزکاری: فاصله‌های اضافی
  return out.replace(/\s+/g, ' ').trim().toLowerCase();
}

/** متن را به آرایه‌ای از واژه‌های لاتین تمیز تبدیل می‌کند */
export function toWords(fgText) {
  return fgText
    .toLowerCase()
    .replace(/[^a-z\s]/g, '')
    .split(/\s+/)
    .filter(Boolean);
}
